func TestLocaleImpl_LanguageTwoLetter(t *testing.T) {
	if lc, err := New("ja"); err != nil {
		t.Error(err)
	} else if x := lc.LanguageTwoLetter(); x != "ja" {
		t.Error(x)
	}

	if lc, err := New("ja-JP"); err != nil {
		t.Error(err)
	} else if x := lc.LanguageTwoLetter(); x != "ja" {
		t.Error(x)
	}

	if lc, err := New("ja-JP.UTF-8"); err != nil {
		t.Error(err)
	} else if x := lc.LanguageTwoLetter(); x != "ja" {
		t.Error(x)
	}

	for _, lt := range languageTagSamples {
		if lc, err := New(lt); err != nil {
			t.Error(lt, err)
		} else if x := lc.LanguageTwoLetter(); len(x) != 0 && len(x) != 2 {
			t.Error(x, err)
		} else {
			t.Log(lt, lc.Language())
		}
	}
}

